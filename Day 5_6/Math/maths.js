Math.round(4.5); // 5 returns x rounded to its nearest integer
Math.ceil(5.6); // 6 returns x rounded up to it nearest integer
Math.floor(-4.4); // 4  returns x rounded down to its nearest integer
Math.trunc(4.7); // returns the integer part of the number
Math.sign(-4); // returns negative, null or positive
Math.pow(8, 2); // 8^2
Math.sqrt(64); // 8
Math.abs(-4.7); // 4.7 returns absolute value
Math.min(0, 150, 5, 8); // 0 returns min value
Math.random(); // returns random number b/w 0 & 1
